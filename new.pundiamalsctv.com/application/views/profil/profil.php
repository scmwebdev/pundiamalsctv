                 <p style="font-weight:bold; font-size:14px">TENTANG PUNDI AMAL SCTV</p>
                 <p>
                    Berbagai persoalan sosial yang terjadi di masyarakat tidak dapat membiarkan SCTV berpangku tangan. Memanfaatkan kelebihannya sebagai media televisi yang memiliki jangkauan nasional di semua kalangan masyarakat, SCTV melalui PUNDI AMAL SCTV menjalankan peran sosial menjadi jembatan antara kelompok masyarakat agar tumbuh keterikatan untuk saling peduli dan membantu sesama. Maka sejak tahun 2000 manajemen SCTV membentuk Pundi Amal SCTV (PAS) sebagai respon atas permintaan pemirsa yang ingin berpartisipasi membantu meringankan penderitaan dan beban sesamanya dengan memberikan sumbangan. </p>
                 <p>
                     Agar memudahkan menyalurkan bantuannya, Pundi Amal SCTV membagi fokus kegiatannya pada empat:  tanggap darurat dan rehabilitasi pasca bencana; pendidikan dini, dasar dan ketrampilan; penanggulangan dan penyuluhan kesehatan; dan pengembangan lingkungan. Agar mudah mengingatnya, keempatnya disebut dengan Empat Pilar Pundi Amal SCTV: Penangan Bencana; Pendidikan; Kesehatan dan Pengembangan Lingkungan.<br />
                    </p>
                 <p>Tim Pundi Amal SCTV adalah para karyawan SCTV yang didedikasikan manajemen SCTV untuk menangani Bidang Pundi Amal. Dalam menjalankan kegiatannya, Tim Pundi Amal SCTV menjalin kerjasama dengan para relawan, mitra dana dan para koresponden Liputan 6 SCTV di seluruh Indonesia. SCTV sebagai perusahaan tempat Pundi Amal SCTV bernaung menanggung biaya operasional semua kegiatan Pundi Amal SCTV sebagai salah satu bentuk tanggungjawab sosial perusahaan. Dengan cara ini pula, dana masyarakat yang terkumpul di Pundi Amal dapat seoptimal mungkin dipergunakan untuk membantu masyarakat yang membutuhkan, sesuai dengan bidang kerja Pundi Amal SCTV.</p>
